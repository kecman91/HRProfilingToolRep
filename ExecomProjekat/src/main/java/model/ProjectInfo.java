package model;

public class ProjectInfo {
	
	private int idProject;
	private int idEployee;
	private String jobResponsibilities;
	private String projectExp;
	
	public int getIdProject() {
		return idProject;
	}
	public void setIdProject(int idProject) {
		this.idProject = idProject;
	}
	public int getIdEployee() {
		return idEployee;
	}
	public void setIdEployee(int idEployee) {
		this.idEployee = idEployee;
	}
	public String getJobResponsibilities() {
		return jobResponsibilities;
	}
	public void setJobResponsibilities(String jobResponsibilities) {
		this.jobResponsibilities = jobResponsibilities;
	}
	public String getProjectExp() {
		return projectExp;
	}
	public void setProjectExp(String projectExp) {
		this.projectExp = projectExp;
	}
	
	
}
